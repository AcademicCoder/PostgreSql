ugu sheegeyno hab amar ah oo shaqadii inoo qabanaysa 

1. DML
2. DDL
3. DCL
4. DQL

1.DDL (Data Defination Langauge)
waa commands ka ugu muhiinsan laf dhabarkana u ah 
shaqo kasta oo aad qabanayso waxa uu ka kooban yahay 3 commands
1. create = waxay inoo qabanaysa 3 shaqo oo ah 
  1. in ay inoo samayso database ka 
  2. in ay inoo samayso table 
  3. in ay inoo samayso virtual table ka

2. truncate  = waxay inoo qabanaysaa inaad table kaagi xogta ku jira halmar aad ku masixi karto mana aha mid inta badan la adeegsado maxa yeeley xogta uu masaxo second gudihii ayuu ku saaraya sidoo kale lamana soo celin karo 

3. drop  = waxay inoo qabanaysa in aad database kaagi ku saari kartid sidoo kale table kaga waxa uu is raacin karaa database ka iyo table ka halmar 
sidoo kale wuu kala reebi karaa 
waa marka ad database ka horeysiiso way wada baxayaan 
sababtoo ah table ku wuxuu ku dhex jiraa database ka 

dhinac kale table ka kaligi waad sari kartaa 
balse table ka iyo xogta ayuu israacinayaa 


2. DML (Data Manupilation Langauge)
wuxuu inoo hayaa commands dhawr nooc ah kuwaas oo sees u ah 
shaqo kasta oo aan qabanayno waxana kamid ah 
1. select 
2. insert 
3. delete 
4. update 

1. select :
waxay inoo qabanaysa shaqo aad u wayn taas oo dhinacyo faro badan ah 
waxa kamid ah in ad kusoo akhrin karto table kaaga xogta ku jirta 
sidoo kale in aad qaybo kamid ah ad xogtada kusoo akhrin karto 
sidoo kale in aad ku ogaan karto kala saraynta xogahaga 
sidoo kale in aad ku ogaan karto tirada guud ee xogtada 
sidoo kal inaad ku samayn karto wax loo yaqaan joins oo inoo soo socda 
select waxa database uu ugu jiraa kaalin aad u wayn 
illa database developers ku ay u sameeyen 
qayb isaga u magacaban oo la idhaah 
DQL (data qeury langauge ) waana select 

2. insert: 
waxay inoo qabanaysa inaad table kaagi xog kusoo gelin karto 
waxana lagu gelin kara illa dhawr nooc 
balse waxaynu kasoo qaadan karnaa laba nooc oo aad u caan ah 

1. in aad sheegto markaad xog galinayso columns yada table kaagu ka kooban yahay kadib xogta siday u kala horeyso 
2. in aad xogta oo qudha ku gelin karto adoon sheegin cloumns yada 

3. delete :
waxay ino qabanaysa in aad ku delete garayn karto 
dhamaan xogta sidoo kale inaad dooran karto 
qaybo kamid ah xogta si aad u delete garayn karto 
waana waxyabaha uu kaga wanagsan yahay ka truncate la idhaah oo aan xaga sare kaga soo hadalnay

4. update:
waxay inoo qabanaysaa in aan xogtii inaga qaldantay 
ku hagaajin karno inagoon masaxin 
sidoo kale update iyo delete waxay xogta halis ku ihiin markaad 
ka tagto statement la idhaah (where)
where waxay kuu sahlaysa inaad si gaara xogtaad rabto 
shaqo uga qaban kartod si where loogu adeegsadana 
waxa ugu wanagsan id number ka qofka ama shayga la badalayo.
