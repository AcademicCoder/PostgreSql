#postgres  waa nooc kamid ah databases ka la isticmalo 
kuwooda aduunka ugu horeya waqti xaadirkan 
hadaba muxu kaga duwan yahay databases ka kale 

1. waa open-source oo ah mid furan oo cidkasta isticmaali karto waxna ku darsan karto 
halka databases ka qaarkood aanad waxba ku darsan karin oo ay shirkado leyihiin 
sidoo kalena ay maamulaan 
balse kan aduunka oo dhan ayaa uu ka dhaxeeyaa waana mid free ah 
2. soo dejintiisu waa mid aad u fudud sidoo kale ku shubistiisu waa mid fudud
waxay ku xidhan tahay operatem system ka aad isticmaalayso 
sidoo kale waana crose-platform oo aan ula jeedno waa mid 
la shaqayn kara operating system kasta sida windows linux mac 
3. barasho ahaan waa mid aad u fudud sidoo kale waa databases ka loo yaqaan RDBM(Relational Database Management System)
4. waxa lagu dhisi kara database yada webapplications ka iyo mobile applciation 
kuwaas oo uu awood xoogan ku leeyahay 
5. waxyaabo uu kaga soocan yahay database yada kale ayaa inoo iman doona 


postgresql muxuu isticmala ama databases ka kale ee jiraaba wax ay ka siman yihin ayaa jira 

1. commands 
2. constraints 
3. joins 
4. iyo kuwo kale oo badan oo aynu arki doono insha allah.


