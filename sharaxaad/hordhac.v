# waa maxay database 
database waa meel aynu ku habaysanayno xogeheena  waxa uu inoo kaydiyaa xog kaas oo aynu computer ka u adeegsano in aynu xogteeni wax kaga badali karno waxna kaga dari karno 
shaykasta oo aynu isticmaalno wuxu leyahay qayb wax kaydisa oo loo yaqaan database;


# siduu u shaqeeyaa :
wuxu u shaqeeyaa qaab electronic ahaan ah 
halka lagu maamulona ay tahay computer wixii kale ee surto gal ah 


# yaa isticmaala database:
waxa isticmala qofkasta oo adeegsanaya technology ga 
sidoo kalena ah qof xogo badan ururinaya sida shirkad dawlad iyo shaqsi caadi ahba

 # ujeedada database laga leeyahay:
 waa in aynu ku kaydisan karno xogeheena gaarka ah iyo kuwo 
 guud ba

 # database la,aanti qofku wax ma dhisi karaa:

database waa halka go,aamisa systemkeena ama software keena 
maxaa yeley ujeedada aynu database u isticmalayno ayaa ah in uu inoo kaydiyo xogta aynu rabno 

#database ku ma programming ka ayuu kami yahay:

database ku waa meesha aynu xogta ku kayd sanayno 
hadaba waxa jira tools inaga caawinaya in aynu database ka la xidhiidhno 
kuwaas oo ah qayb isaga u gaar ah oo loo yaqaan query langauge 
waana ay faro badan yihin 

waxa kamid ah :
1. sql server 
2. postgresql 
3. mysql 
4. mongodb 



#database ka barashadiisa keebaa i anfacaya:

ugu horeyn waxaynu isla eegeyna imisa database ayaa jira 
aduunku isticmala
haa wa waydiin mudan 
1. nooc looyaqaan RDBM (Relational Database Management System);
2. NoSQL
hadaba labada database aya waxay ku kala duwan yihiin 
in ka hore uu leeyahay wax loo yaqaan tables sidoo kale columns iyo rows waxa usii dheer in uu yahay mid ay tables kiisu isku xidhmayaan 

nooca labaad ayaa ah mid isna database ah balse 
habka uu xogta u kaydiyo ayaa ah in uu isticmalo 
document sidoo kale key-value iyo type-value 
manalaha wax isku xidh ah 

hadaba barasho ahaan kee ayaa i anfacaya ama aan horeysiya
